    public static void main(String args[]) throws FileNotFoundException, IOException, ClassNotFoundException, InterruptedException, InstantiationException, IllegalAccessException, UnsupportedLookAndFeelException{
        UIManager.setLookAndFeel(new DarculaLaf());

        Splash splash = new Splash();
        
        /* Create and display the form */
        java.awt.EventQueue.invokeLater(new Runnable() {
            public void run() {
                splash.setVisible(true);
            }
        });
        Thread.sleep(ThreadLocalRandom.current().nextInt(2500, 5000));
        splash.dispose();
        Application application = new Application();
        java.awt.EventQueue.invokeLater(new Runnable() {
            public void run() {
                application.setVisible(true);
            }
        });
        
        File folder = new File("data");
        File[] files = folder.listFiles();
        ArrayList<String> fileNames = new ArrayList<String>();
        for(int i = 0; i < files.length; i++){
            if(files[i].isFile()){
                fileNames.add(files[i].getName());
            }
        }
        DefaultListModel dlm = new DefaultListModel();
        for(int j = 0; j < fileNames.size(); j++){
            dlm.addElement(fileNames.get(j));
        }
        application.jList1.setModel(dlm);
        
        DS200 a = new DS200();
        DS200List list = new DS200List("Test", "John", "Jim");
        list.addMachine(a);
        System.out.println(list.toString());
        System.out.println(list);
        System.out.println(list.getList().get(0));
        
        //File Output
        File dir = new File("data");
        dir.mkdir();
        FileOutputStream fos = new FileOutputStream(System.getProperty("user.dir") + "/data/list.cray");
        ObjectOutputStream oos = new ObjectOutputStream(fos);
        oos.writeObject(list);
        oos.close();
        fos.close();
        System.out.println("Saved list to /data/list.cray");
        
        //File Input
        FileInputStream fis = new FileInputStream("data/list.cray");
        ObjectInputStream ois = new ObjectInputStream(fis);
        DS200List readList = (DS200List)ois.readObject();
        ois.close();
        fis.close();
        
        System.out.println(readList);
    }

